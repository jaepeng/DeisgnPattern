package com.jae.bulider.improve.mytest;

public class Candy {
    private String Suger;
    private String pack;
    private String bake;

    public String getSuger() {
        return Suger;
    }

    public void setSuger(String suger) {
        Suger = suger;
    }

    public String getPack() {
        return pack;
    }

    public void setPack(String pack) {
        this.pack = pack;
    }

    public String getBake() {
        return bake;
    }

    public void setBake(String bake) {
        this.bake = bake;
    }
}
